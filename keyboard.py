@router.callback_query(lambda call: call.data.split(':')[0] in ["details", "late"])
async def handle_inline_button(call: types.CallbackQuery):
    user_id = call.message.chat.id
    action, number, text = call.data.split(':')
    logger.info(
        f"–ü–æ–ª—É—á–µ–Ω –∑–∞–ø—Ä–æ—Å –æ—Ç –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è {user_id} –¥–ª—è –Ω–æ–º–µ—Ä–∞ {number} —Å –¥–µ–π—Å—Ç–≤–∏–µ–º {action}.")

    try:
        if action == "details":
            await call.message.edit_text(  # –û–±–Ω–æ–≤–ª—è–µ–º —Å–æ–æ–±—â–µ–Ω–∏–µ
                text=f"üìã –î–µ—Ç–∞–ª–∏ —Ä–µ–π—Å–∞ {number}:\n\nüöç –ù–æ–º–µ—Ä: {number}\nüìç –ú–∞—Ä—à—Ä—É—Ç: –ù–æ–≤–æ—Å–∏–±–∏—Ä—Å–∫-–ö—Ä–∞—Å–Ω–æ—è—Ä—Å–∫\nüë§ –í–æ–¥–∏—Ç–µ–ª—å: –ë–æ—á–∫–∞—Ä–µ–≤ –î–µ–Ω–∏—Å\nüöó –ê–≤—Ç–æ: 195 –°–æ–ª–µ—Ä—Å",
                reply_markup=InlineKeyboardMarkup(inline_keyboard=[
                    [InlineKeyboardButton(
                        text="üîô –ù–∞–∑–∞–¥", callback_data=f"back:{number}:{text}")]
                ])
            )

        elif action == "late":
            await call.message.edit_text(  # –û–±–Ω–æ–≤–ª—è–µ–º —Å–æ–æ–±—â–µ–Ω–∏–µ, –∞ –Ω–µ –æ—Ç–ø—Ä–∞–≤–ª—è–µ–º –Ω–æ–≤–æ–µ
                text=f"‚ö†Ô∏è –í—ã —É–≤–µ—Ä–µ–Ω—ã, —á—Ç–æ —Ö–æ—Ç–∏—Ç–µ —Å–æ–æ–±—â–∏—Ç—å –æ –∑–∞–¥–µ—Ä–∂–∫–µ —Ä–µ–π—Å–∞ {number}?",
                reply_markup=InlineKeyboardMarkup(inline_keyboard=[
                    [InlineKeyboardButton(
                        text="‚úÖ –î–∞", callback_data=f"yes:{number}:{text}")],
                    [InlineKeyboardButton(
                        text="‚ùå –ù–µ—Ç", callback_data=f"no:{number}:{text}")],
                    [InlineKeyboardButton(
                        text="üîô –ù–∞–∑–∞–¥", callback_data=f"back:{number}:{text}")]
                ])
            )

        await call.answer()  # –ó–∞–∫—Ä—ã–≤–∞–µ–º callback-–∑–∞–ø—Ä–æ—Å, —á—Ç–æ–±—ã –∫–Ω–æ–ø–∫–∏ –Ω–µ –≤–∏—Å–µ–ª–∏

    except Exception as e:
        logger.error(
            f"–û—à–∏–±–∫–∞ –ø—Ä–∏ –æ–±—Ä–∞–±–æ—Ç–∫–µ callback-–∫–Ω–æ–ø–∫–∏ –æ—Ç –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è {user_id}: {e}")


